package com.myproject.library.controller;

import com.myproject.library.dto.AuthorDto;
import com.myproject.library.dto.BookDto;
import com.myproject.library.service.AuthorService;
import lombok.RequiredArgsConstructor;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequiredArgsConstructor
public class AuthorController {

    private final AuthorService authorService;
    @GetMapping("/author/{id}")
    AuthorDto getAuthorById(@PathVariable("id") Long id) {
        return authorService.getAuthorById(id);
    }

    @GetMapping("/author/byNameV1")
    AuthorDto getAuthorByNameUsingCriteria(@RequestParam("name") String name) {
        return authorService.getAuthorByNameV1(name);
    }
    @GetMapping("/author/byNameV2")
    AuthorDto getAuthorByNameUsingQuery(@RequestParam("name") String name) {
        return authorService.getAuthorByNameV2(name);
    }
    @GetMapping("/author/byNameV3")
    AuthorDto getAuthorByNameUsingAutoGeneratedQuery(@RequestParam("name") String name) {
        return authorService.getAuthorByNameV3(name);
    }
}
