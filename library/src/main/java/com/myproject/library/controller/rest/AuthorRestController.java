package com.myproject.library.controller.rest;

import com.myproject.library.dto.AuthorCreateDto;
import com.myproject.library.dto.AuthorDto;
import com.myproject.library.dto.AuthorUpdateDto;
import com.myproject.library.service.AuthorService;
import io.swagger.v3.oas.annotations.responses.ApiResponse;
import io.swagger.v3.oas.annotations.security.SecurityRequirement;
import jakarta.validation.Valid;
import lombok.RequiredArgsConstructor;
import org.springframework.web.bind.annotation.*;


@RestController
@RequiredArgsConstructor
@SecurityRequirement(name = "library-users")
@ApiResponse
public class AuthorRestController {

    private final AuthorService authorService;
    @GetMapping("/author/{id}")
    AuthorDto getAuthorById(@PathVariable("id") Long id) {
        return authorService.getAuthorById(id);
    }

    @GetMapping("/author/byNameV1")
    AuthorDto getAuthorByNameUsingCriteria(@RequestParam("name") String name) {
        return authorService.getAuthorByNameV1(name);
    }
    @GetMapping("/author/byNameV2")
    AuthorDto getAuthorByNameUsingQuery(@RequestParam("name") String name) {
        return authorService.getAuthorByNameV2(name);
    }
    @GetMapping("/author/byNameV3")
    AuthorDto getAuthorByNameUsingAutoGeneratedQuery(@RequestParam("name") String name) {
        return authorService.getAuthorByNameV3(name);
    }
    @PostMapping("/author/create")
    AuthorDto createAuthor(@RequestBody @Valid AuthorCreateDto authorCreateDto) {
        return authorService.createAuthor(authorCreateDto);
    }
    @PutMapping("/author/update")
    AuthorDto updateAuthor(@RequestBody @Valid AuthorUpdateDto authorUpdateDto) {
        return authorService.updateAuthor(authorUpdateDto);
    }
    @DeleteMapping("/author/delete/{id}")
    void updateAuthor(@PathVariable("id") Long id) {
        authorService.deleteAuthor(id);
    }
}
